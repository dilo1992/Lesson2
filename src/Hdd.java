public class Hdd {
    private String name;
    private int volume;
    private String type;

    public Hdd() {
        this.name = name;
        this.volume = volume;
        this.type = type;
    }

    public void HddWithParametres(String name, int volume) {
        this.name = name;
        this.volume = volume;
        this.type = type;
    }
}
